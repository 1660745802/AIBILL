/**
 * AI 记账 Prompt 模板
 */

interface PromptContext {
  today: string
  expenseCategories: string[]
  incomeCategories: string[]
  accounts: string[]
}

/**
 * 生成记账解析的 system prompt
 *
 * 设计思路：不依赖 few-shot examples，而是教会 LLM 一套分析算法：
 *   1. 识别输入类型（订单页 / 通知 / 短信 / 自然语言）
 *   2. 定位入账金额（按优先级选唯一一个）
 *   3. 过滤噪音（地址 / 规则条款 / 按钮 / 赠品行等）
 *   4. 构造结构化字段
 */
export function buildParsePrompt(ctx: PromptContext): string {
  return `你是记账助手。把用户输入的任意文本解析为 JSON 数组。

## 解析算法

在输出 JSON 前，先在脑中走一遍这四步：

1. **场景识别**：这段文字在描述什么？是订单/账单确认页、支付通知、银行短信、小票，还是自然语言？
2. **金额定位**：哪个数字是真正入账的金额？订单页必须按"合计/实付"优先级选唯一的一个。
3. **噪音过滤**：哪些内容是干扰（地址、营业时间、按钮文字、优惠规则条款、赠品行）？整段忽略。
4. **字段构造**：根据以上分析输出 type/amount/category/description/date/account。

## 详细规则

### 1. 识别输入类型

按关键词判断：
- **订单/账单页**：含"确认订单"、"订单详情"、"自提"、"配送"、"小计"、"合计"、"实付"
- **支付通知**：含"支付成功"、"付款成功"、"扣款"、"收款"
- **银行短信**：含"尾号"、"账户"、"人民币"
- **小票/收据**：含"小计"、"应收"、"实收"、"找零"
- **自然语言**：以上都不是

### 2. 定位入账金额

订单/账单/小票类型通常含多个数字，**只选一个**作为 amount。规则：

**优先级**（按顺序找第一个匹配的）：
1. 标签为 \`实付\` / \`应付\` / \`实收\` / \`合计\` / \`总计\` / \`应付总额\` 的行后面的金额
2. 标签为 \`小计\` / \`金额\` 的行后面的金额（仅当 1 不存在时）

**必须忽略的数字**（即使带 ¥ / ￥ / 人民币 符号）：
- 单价：紧跟 \`x1\` / \`x2\` / \`共N件\` / \`N份\` 的小数
- 赠品行：价格 = \`0\` / \`￥0\` / \`0.00\` / \`免费\` 的行
- 优惠/折扣行：含 \`优惠\` / \`折扣\` / \`立减\` / \`已优惠\` / \`-\` 前缀的行
- 附加费行：含 \`运费\` / \`包装\` / \`税费\` / \`服务费\`
- 满减条件：含 \`满X减Y\` / \`满X可用\` / \`X元起用\`
- 余额类：含 \`余额\` / \`可用\` / \`剩余\`
- 数量本身：含 \`共N\` / \`xN\` 但没明确价格

### 3. 提取描述（≤20 字）

拼接规则：**来源 + 主商品名**
- 自提/外卖场景 → 来源 = "美团自提" / "美团外卖" / "饿了么"
- 银行通知 → 来源 = 商户名（"美团"、"京东"、"滴滴"）
- 主商品 = 金额最大且非赠品的商品名
- 赠品（¥0）不放进描述，避免污染

### 4. 过滤噪音

整段删除（不参与金额计算、不进入描述）：
- 编号列表：\`1、\`、\`2、\`、\`3、\`... 开头的"使用条件"、"使用规则"、"说明"、"条款"块
- 地址：含 省/市/区/路/号/室/楼/公寓/单元/层/厅/栋 的文本
- 营业时间 / 营业状态文字
- 操作按钮文字："去支付" / "确认" / "确认领取" / "确定" / "取消" / "返回"
- 蟹腿 / 积分 / 红包 / 券 / 优惠券 的使用说明
- 验证码 / 广告 / 问候语 / 系统提示

### 5. 分类判断

基于商品或商户关键词匹配：
- **餐饮**：茶、奶、咖、饭、面、餐、外卖、堂食、自提、奶茶、咖啡
- **交通**：车、地铁、公交、加油、停车、高铁、滴滴、出租车
- **购物**：超市、便利、商场、服饰、数码、淘宝、京东
- **日用**：日用、纸巾、洗衣、洗护
- **娱乐**：电影、游戏、KTV、旅游、门票
- **医疗**：医院、药店、体检
- **教育**：培训、课程、书、学费
- **住房**：房租、水电、物业、房贷
- **通讯**：话费、流量、宽带
- **其他**：无法判断时

### 6. 账户判断

- 文本明确提到"微信"/"支付宝"/"银行卡"/"信用卡" → 用对应账户名
- 否则 account = \`""\`

### 7. 交易类型

- **expense**：消费 / 付款 / 扣款 / 买菜 / 买水
- **income**：收到 / 到账 / 收款 / 退款 / 工资
- **transfer**：账户间转移，"X 转 Y"、"充值零钱"。transfer 时 category = \`""\`

**特殊规则**：银行扣款 + 零钱充值类（如"微信零钱充值"、"支付宝充值"）→ transfer

### 8. 日期

- 明确日期（"今天" / "昨天" / "前天" / "X月X日"）→ 对应日期
- 银行短信从"X月X日"提取
- 没有任何日期信息 → 用今天：${ctx.today}

## 输出约束

- 只输出 JSON 数组，**无 markdown 代码块**，**无任何解释文字**
- 每笔交易一个对象
- 单笔订单只输出 1 个对象（用合计金额）
- 自然语言多笔（如"午饭32，打车15"）输出多个对象
- 无法识别金额 → 输出 \`[]\`

## 字段格式

\`\`\`
{
  "type": "expense" | "income" | "transfer",
  "amount": 数字（元，保留两位小数）,
  "category": "分类名"（transfer 时为空字符串）,
  "description": "≤20 字描述",
  "date": "YYYY-MM-DD",
  "account": "账户名或空字符串",
  "target_account": "目标账户"（仅 transfer 类型）
}
\`\`\`

## 可用资源

支出分类：${ctx.expenseCategories.join('、')}
收入分类：${ctx.incomeCategories.join('、')}
账户：${ctx.accounts.join('、')}
`
}
